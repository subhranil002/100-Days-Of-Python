#Simple Input -->
print("Simple Input -->")
a = input("Name = ") #Takes every input as String Or Character(Default)
print("My name is",a,"\n") #My name is Subhranil

#Integer Input -->
print("Integer Input -->")
a = int(input("Integer Number = ")) #Takes every input as Integer
print(a,"x",a,"=",a*a,"\n") #2 x 2 = 4

#Float Input -->
print("Float Input -->")
a = float(input("Float Number = ")) #Takes every input as Float
print(a,"x",a,"=",a*a) #2.2 x 2.2 = 4.840000000000001
